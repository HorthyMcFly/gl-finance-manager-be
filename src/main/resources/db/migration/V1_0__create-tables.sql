CREATE TABLE FM_USER
(
    ID             INT AUTO_INCREMENT,
    CONSTRAINT     FM_USER_PK PRIMARY KEY (ID),
    USERNAME       VARCHAR(20) NOT NULL UNIQUE,
    PASSWORD       VARCHAR(100)  NOT NULL,
    ADMIN          TINYINT NOT NULL,
    ACTIVE         TINYINT NOT NULL
);

CREATE TABLE PERIOD
(
    ID             INT AUTO_INCREMENT,
    CONSTRAINT     PERIOD_PK PRIMARY KEY (ID),
    START_DATE     DATE NOT NULL,
    END_DATE       DATE NOT NULL,
    ACTIVE         TINYINT NOT NULL
);

CREATE TABLE INCOME
(
    ID             INT AUTO_INCREMENT,
    CONSTRAINT     INCOME_PK PRIMARY KEY (ID),
    FM_USER_ID     INT,
    CONSTRAINT     INCOME_USER_ID_FK FOREIGN KEY (FM_USER_ID) REFERENCES FM_USER(ID),
    PERIOD_ID      INT,
    CONSTRAINT     INCOME_PERIOD_ID_FK FOREIGN KEY (PERIOD_ID) REFERENCES PERIOD(ID),
    AMOUNT         NUMERIC(11, 2) NOT NULL,
    SOURCE         VARCHAR(30) NOT NULL,
    COMMENT        VARCHAR(100)  NOT NULL
);

CREATE TABLE LOAN
(
    ID                INT AUTO_INCREMENT,
    CONSTRAINT        LOAN_PK PRIMARY KEY (ID),
    FM_USER_ID        INT,
    CONSTRAINT        LOAN_USER_ID_FK FOREIGN KEY (FM_USER_ID) REFERENCES FM_USER(ID),
    AMOUNT            NUMERIC(20, 2) NOT NULL,
    NAME              VARCHAR(30) NOT NULL,
    INTEREST_RATE     NUMERIC(5, 2) NOT NULL,
    MONTHLY_REPAYMENT NUMERIC(20, 2) NOT NULL
)